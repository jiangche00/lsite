    {
        $students = DB::select('select * from students');
        $keys = Redis::command('keys', ['*']);
        $records = [];
        foreach ($keys as $key) {
            $record = array('key' => $key, 'value' => Redis::get($key));
            array_push($records, $record);
        }
        return view('students.index', ['students' => $students, 'records' => $records]);
    }

